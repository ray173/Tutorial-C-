//INCREMET DAN DECREMENT

#include<iostream>
using namespace std;
int main( ){
    int a=5;
    int b=5;
    
    //postincrement
    cout<< a << endl;
    cout<< a++ <<endl;
    cout<< a << endl;
    
    cout<<endl;
    
    //preincrement
    cout<< b << endl;
    cout<< ++b <<endl;
    cout<< b << endl;
}
